package wlow01._2_calculation;

public class _3_VariedKeywordsCalc {
    public static void main(String[] args) {
        //不同关键字的相互运算.
        double a = 18.563214013;
        short b = 2;
        System.out.println(b);//18

        System.out.println(1 + 'a');//98, a=97参考ASCII码表
        System.out.println(1 + "a");//1a
        System.out.println(1 + a);
    }
}
